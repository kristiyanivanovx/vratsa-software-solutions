function solve(x) 
{
    switch (x) {
        case 0:
            console.log("Zero");
            break;
    
        case 1:
            console.log("One");
            break;

        case 2:
            console.log("Two");
            break;
 
        case 3:
            console.log('Three');
            break;
 
        case 4:
            console.log('Four');
            break;
 
        case 5:
            console.log('Five');
            break;
 
        case 6:
            console.log('Six');
            break;
 
        case 7:
            console.log('Seven');
            break;
 
        case 8:
            console.log('Eight');
            break;
 
        case 9:
            console.log('Nine');
            break;
 
            
        default:
            console.log('not a digit');
            break;
    }
   
}

solve(2)
solve(1)
solve(0)
solve(5)
solve(-0.1)
solve('hi')
solve(9)
solve(10)
